"""
fidelity/level2_cfd.py
----------------------
Level 2 — 3-D Full-Wing RANS CFD Evaluator

Truth layer for design validation. Implements the workflow for submitting
and monitoring a full 3-D RANS simulation of the assembled wing geometry
(mainplane + flap + endplates).

Pipeline
--------
1. Export multi-element 3-D geometry (STL / IGES / OpenFOAM blockMesh)
2. Generate mesh (snappyHexMesh or Gmsh extrusion)
3. Write OpenFOAM or SU2-3D case from template
4. Submit (local subprocess or HPC via ssh/sbatch hook)
5. Poll for convergence / completion
6. Parse integrated forces + moment from postProcessing/
7. Return FidelityResult with full 3-D aerodynamic data

When a real solver is not present the evaluator runs in STUB mode.
"""

from __future__ import annotations

import json
import os
import subprocess
import tempfile
import time
import uuid
from dataclasses import dataclass, field
from pathlib import Path
from typing import Any, Dict, List, Optional

from fidelity.base import FidelityEvaluator, FidelityLevel, FidelityResult

# ── Environment config ────────────────────────────────────────────────────────
_OF_BIN         = os.environ.get("OPENFOAM_BIN", "simpleFoam")
_CFD3D_WORK_DIR = Path(os.environ.get("CFD3D_WORK_DIR", "/tmp/wingopt_cfd3d"))
_CFD3D_WORK_DIR.mkdir(parents=True, exist_ok=True)

_L2_CL_UNCERTAINTY = 0.04   # ±4% for converged 3-D RANS on fine mesh
_L2_CD_UNCERTAINTY = 0.06   # ±6%


